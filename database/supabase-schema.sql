-- Extension pour les UUID et la recherche plein texte
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
CREATE EXTENSION IF NOT EXISTS "pg_trgm";

-- ============================================================
-- Table des articles
-- ============================================================
CREATE TABLE IF NOT EXISTS articles (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    titre VARCHAR(255) NOT NULL,
    date VARCHAR(50) NOT NULL,
    categorie VARCHAR(50) CHECK (categorie IN ('Transports', 'Aviation Civile', 'Météorologie')) NOT NULL,
    resume TEXT NOT NULL,
    contenu TEXT,
    image VARCHAR(500),
    imageKey VARCHAR(100),
    une BOOLEAN DEFAULT FALSE,
    statut VARCHAR(20) CHECK (statut IN ('Brouillon', 'Publié')) DEFAULT 'Brouillon',
    vues BIGINT DEFAULT 0,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Index pour les articles
CREATE INDEX IF NOT EXISTS idx_articles_categorie ON articles(categorie);
CREATE INDEX IF NOT EXISTS idx_articles_statut ON articles(statut);
CREATE INDEX IF NOT EXISTS idx_articles_une ON articles(une);
CREATE INDEX IF NOT EXISTS idx_articles_date ON articles(date);
CREATE INDEX IF NOT EXISTS idx_articles_created_at ON articles(created_at);
CREATE INDEX IF NOT EXISTS idx_articles_recentes ON articles(statut, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_articles_categorie_statut ON articles(categorie, statut);
CREATE INDEX IF NOT EXISTS idx_articles_une_statut ON articles(une, statut);

-- Index pour la recherche plein texte
CREATE INDEX IF NOT EXISTS ft_articles_search ON articles USING gin(titre gin_trgm_ops);
CREATE INDEX IF NOT EXISTS ft_articles_resume_search ON articles USING gin(resume gin_trgm_ops);

-- Trigger pour mettre à jour updated_at
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ language 'plpgsql';

CREATE TRIGGER update_articles_updated_at 
    BEFORE UPDATE ON articles 
    FOR EACH ROW 
    EXECUTE FUNCTION update_updated_at_column();

-- ============================================================
-- Table des paramètres du site
-- ============================================================
CREATE TABLE IF NOT EXISTS settings (
    key VARCHAR(100) PRIMARY KEY,
    value TEXT,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Trigger pour mettre à jour updated_at
CREATE TRIGGER update_settings_updated_at 
    BEFORE UPDATE ON settings 
    FOR EACH ROW 
    EXECUTE FUNCTION update_updated_at_column();

-- ============================================================
-- Table des utilisateurs
-- ============================================================
CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR(50) UNIQUE NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL,
    role VARCHAR(20) CHECK (role IN ('Admin', 'Super Admin', 'Editeur')) DEFAULT 'Editeur',
    actif BOOLEAN DEFAULT TRUE,
    last_login TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Index pour les utilisateurs
CREATE INDEX IF NOT EXISTS idx_users_username ON users(username);
CREATE INDEX IF NOT EXISTS idx_users_email ON users(email);
CREATE INDEX IF NOT EXISTS idx_users_actif ON users(actif);

-- ============================================================
-- Table pour les logs des modifications
-- ============================================================
CREATE TABLE IF NOT EXISTS article_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    article_id BIGINT REFERENCES articles(id) ON DELETE CASCADE,
    action VARCHAR(20) CHECK (action IN ('INSERT', 'UPDATE', 'DELETE')),
    ancienne_valeur JSONB,
    nouvelle_valeur JSONB,
    utilisateur VARCHAR(50),
    date_action TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Index pour les logs
CREATE INDEX IF NOT EXISTS idx_article_logs_article_id ON article_logs(article_id);
CREATE INDEX IF NOT EXISTS idx_article_logs_date_action ON article_logs(date_action);

-- ============================================================
-- Vues pour les requêtes fréquentes
-- ============================================================

-- Vue pour les articles publiés uniquement
CREATE OR REPLACE VIEW v_articles_publies AS
SELECT 
    id, titre, date, categorie, resume, contenu, image, imageKey, une, vues, created_at, updated_at
FROM articles 
WHERE statut = 'Publié'
ORDER BY created_at DESC;

-- Vue pour les statistiques des articles
CREATE OR REPLACE VIEW v_articles_stats AS
SELECT 
    categorie,
    COUNT(*) as total,
    SUM(vues) as total_vues,
    COUNT(CASE WHEN statut = 'Publié' THEN 1 END) as publies,
    COUNT(CASE WHEN statut = 'Brouillon' THEN 1 END) as brouillons
FROM articles 
GROUP BY categorie;

-- ============================================================
-- Politiques de sécurité RLS (Row Level Security)
-- ============================================================

-- Activer RLS sur les tables
ALTER TABLE articles ENABLE ROW LEVEL SECURITY;
ALTER TABLE settings ENABLE ROW LEVEL SECURITY;
ALTER TABLE users ENABLE ROW LEVEL SECURITY;
ALTER TABLE article_logs ENABLE ROW LEVEL SECURITY;

-- Politiques pour la table articles (lecture publique)
CREATE POLICY "Les articles publiés sont visibles par tous" ON articles
    FOR SELECT USING (statut = 'Publié');

CREATE POLICY "Les administrateurs peuvent voir tous les articles" ON articles
    FOR ALL USING (
        auth.jwt() ->> 'role' IN ('Admin', 'Super Admin')
    );

-- Politiques pour la table settings (lecture publique, écriture admin)
CREATE POLICY "Tout le monde peut voir les settings" ON settings
    FOR SELECT USING (true);

CREATE POLICY "Les administrateurs peuvent modifier les settings" ON settings
    FOR ALL USING (
        auth.jwt() ->> 'role' IN ('Admin', 'Super Admin')
    );

