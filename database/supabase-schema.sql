-- Fonction pour incrémenter les vues
CREATE OR REPLACE FUNCTION incrementer_vues(article_id_param BIGINT)
RETURNS VOID AS $$
BEGIN
    UPDATE articles 
    SET vues = vues + 1 
    WHERE id = article_id_param;
END;
$$ LANGUAGE plpgsql;

